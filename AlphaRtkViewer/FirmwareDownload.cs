        private static int CheckPromUniqueTag(byte[] fwData)
        {
            byte[] checkPattern = { 0x21, 0x22, 0x23, 0x24, 0x26, 0x2A, 0x28, 0x29,
                         0x2F, 0x3A, 0x3B, 0x3C, 0x3D, 0x3E, 0x3F, 0x40 };

            int addr = -1;
            int i, j;
            for(i = 0; i < fwData.Length - checkPattern.Length + 1; ++i)
            {
                if(fwData[i] == checkPattern[0] && fwData[i + 1] == checkPattern[1])
                {
                    for (j = 2; j < checkPattern.Length; ++j)
                    {
                        if(fwData[i + j] != checkPattern[j])
                        {
                            break;
                        }
                    }
                    if(j == checkPattern.Length)
                    {
                        addr = i - 16;
                        break;
                    }
                }
            }
            if(addr >= 0)
            {
                for (i = addr; i < addr + 16; ++i)
                {
                    if(fwData[i] != 0xFF)
                    {
                        return -1;
                    }
                }
            }
            return addr;
        }
