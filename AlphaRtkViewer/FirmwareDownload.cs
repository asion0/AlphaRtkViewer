        public static string GetRomModeTag(GpsSerial g, int downloadBaudIdx, UpdateProgressFunction pf)
        {
            GPS_RESPONSE rep = GPS_RESPONSE.NONE;
            int baudIdx = g.GetBaudRateIndex();
            if (downloadBaudIdx != g.GetBaudRateIndex())
            {
                rep = g.ConfigureSerialPort(2000, downloadBaudIdx, Attributes.Temporarily);
