            }

            string checkCmd = "DUMP = 1036284 8 ";
            string ret = g.SendDumpLoaderCmd(2000, checkCmd);
            g.ReOpen(baudIdx);
            return ret;
        }

        private static bool _DoDownloadFirmware(GpsSerial g, string fw, int downloadBaudIdx, bool isSlave, UpdateProgressFunction pf)
        {
            byte[] fwData = File.ReadAllBytes(fw);
            if (fwData.Length <= 0)
            {
                return false;
            }

            GPS_RESPONSE rep = GPS_RESPONSE.NONE;
            if (!isSlave && downloadBaudIdx != g.GetBaudRateIndex())
            {
                rep = g.ConfigureSerialPort(2000, downloadBaudIdx, Attributes.Temporarily);
                if (GPS_RESPONSE.ACK != rep)
                {
                    return false;
                }
                g.ReOpen(downloadBaudIdx);
            }

            if (isSlave)
            {
                rep = g.EnterSlavePassThrough(2000, true, true);
                if (GPS_RESPONSE.ACK != rep)
                {
                    return false;
                }
                Thread.Sleep(2000);
            }

            rep = g.SendLoaderDownload(2000, downloadBaudIdx, true);
            if (GPS_RESPONSE.OK != rep)
            {
                return false;
            }

            rep = g.UploadLoader(2000, LoaderData.GenericLoader);
            if (GPS_RESPONSE.OK != rep)
            {
                return false;
            }

            rep = g.SendRomBinSize(3000, fwData.Length, SkytraqTools.CalcPromRawCheckSum(fwData));
            if (GPS_RESPONSE.OK != rep)
            {
                return false;
            }

            const int nFlashBytes = 8 * 1024;
            const int headerSize = 3;

            byte[] header = new byte[headerSize];
            int lSize = fwData.Length;
            int sentBytes = 0;
            int totalByte = 0;
            UInt16 sequence = 0;
            int rawItr = 0;
            int failCount = 0;

            pf(sentBytes, fwData.Length);
            while (lSize > 0)
            {
                sentBytes = (lSize >= nFlashBytes) ? nFlashBytes : lSize;
                totalByte += sentBytes;

                header[0] = (byte)(sequence >> 24 & 0xFF);
                header[1] = (byte)(sequence & 0xff);
                header[2] = SkytraqTools.CalcCheckSum16(fwData, rawItr, sentBytes);
                rep = g.SendDataWaitStringAck(fwData, rawItr, sentBytes, 2000, "OK\0");
                if (rep == GPS_RESPONSE.OK)
                {
                    pf(totalByte - 1, fwData.Length);
                    sequence++;
                    lSize -= sentBytes;
                    rawItr += nFlashBytes;
                }
                else
                {
                    if (++failCount > 0)
                    {
                        return false;
                    }
                    continue;
                }
                failCount = 0;
            }
            rep = g.WaitStringAck(10000, "END\0");
            if (GPS_RESPONSE.OK != rep)
            {
                return false;
            }
            pf(fwData.Length, fwData.Length);

            if (isSlave)
            {   //Leave slave pass-through
                rep = g.EnterSlavePassThrough(2000, false, true);
                if (GPS_RESPONSE.ACK != rep)
                {
                    return false;
                }
            }
            return true;
        }

        private static byte[] GetLzmaOfFile(string f)
        {
            string updaterPath = AppTools.GetRtkViewerLzmaPath();
            string output = AppTools.GetRtkViewerDownloadFolder(false) + "\\p.7z";
            if (!AppTools.ExtractEmbeddedFile("RtkViewer.lzma.exe", updaterPath))
            {
                return null;
            } 
            string param = string.Format("e \"{0}\" {1} -d13", f, output);
            //AppTools.LaunchExe(updaterPath, string.Format("\"{0}\"", param));
            bool b = AppTools.LaunchExeWait(updaterPath, param, 1000);
            return File.ReadAllBytes(output);
        }

        private static int CheckPromUniqueTag(byte[] fwData)
        {
            byte[] checkPattern = { 0x21, 0x22, 0x23, 0x24, 0x26, 0x2A, 0x28, 0x29,
                         0x2F, 0x3A, 0x3B, 0x3C, 0x3D, 0x3E, 0x3F, 0x40 };

            int addr = -1;
            int i, j;
            for(i = 0; i < fwData.Length - checkPattern.Length + 1; ++i)
            {
                if(fwData[i] == checkPattern[0] && fwData[i + 1] == checkPattern[1])
                {
                    for (j = 2; j < checkPattern.Length; ++j)
                    {
                        if(fwData[i + j] != checkPattern[j])
                        {
                            break;
                        }
                    }
                    if(j == checkPattern.Length)
                    {
                        addr = i - 16;
                        break;
                    }
                }
            }
            if(addr >= 0)
            {
                for (i = addr; i < addr + 16; ++i)
                {
                    if(fwData[i] != 0xFF)
                    {
                        return -1;
                    }
                }
            }
            return addr;
        }

        private static bool _DoDownloadPhoenixFirmware(GpsSerial g, string fw, int downloadBaudIdx, bool isSlave, UpdateProgressFunction pf)
        {
            const int LzmaHeaderSize = 13;
            byte[] raw = File.ReadAllBytes(fw);
            int tagPos = CheckPromUniqueTag(raw);
            if (tagPos < 0)
            {
                return false;
            }
            byte ck = SkytraqTools.CalcPromRawCheckSum(raw);

            byte[] fwData = GetLzmaOfFile(fw);
            double r = (double)raw.Length / fwData.Length;

            if (fwData == null || fwData.Length <= 0)
            {
                return false;
            }

            GPS_RESPONSE rep = GPS_RESPONSE.NONE;
            if (downloadBaudIdx != g.GetBaudRateIndex())
            {
                rep = g.ConfigureSerialPort(2000, downloadBaudIdx, Attributes.Temporarily);
                if (GPS_RESPONSE.ACK != rep)
                {
                    return false;
                }
                g.ReOpen(downloadBaudIdx);
            }

            rep = g.SendLoaderDownload(2000, downloadBaudIdx, true);
            if (GPS_RESPONSE.OK != rep)
            {
                return false;
            }

            rep = g.UploadLoader(2000, LoaderData.PhoenixLoader);
            if (GPS_RESPONSE.OK != rep)
            {
                return false;
            }

            rep = g.SendRomBinSize8(3000, fwData, ck, tagPos, 0);
            if (GPS_RESPONSE.OK != rep)
            {
                return false;
            }

            const int nFlashBytes = 8 * 1024;
            int lSize = fwData.Length - LzmaHeaderSize;
            int sentBytes = 0;
            int totalByte = 0;
            int rawItr = LzmaHeaderSize;
            int failCount = 0;

            pf(0, raw.Length);
            while (lSize > 0)
            {
                sentBytes = (lSize >= nFlashBytes) ? nFlashBytes : lSize;
                totalByte += sentBytes;

                rep = g.SendDataWaitStringAck(fwData, rawItr, sentBytes, 3000, "OK\0");
                if (rep == GPS_RESPONSE.OK)
                {
                    lSize -= sentBytes;
                    rawItr += nFlashBytes;
                    pf((int)(rawItr * r), raw.Length);
                }
                else
                {
                    if (++failCount > 0)
                    {
                        return false;
                    }
                    continue;
                }
                failCount = 0;
            }
            rep = g.WaitStringAck(10000, "END\0");
            if (GPS_RESPONSE.OK != rep)
            {
                return false;
            }
            pf(raw.Length, raw.Length);
            return true;
        }
        private static bool _DoDownloadPhoenixFirmwareNoLzma(GpsSerial g, string fw, int downloadBaudIdx, bool isSlave, UpdateProgressFunction pf)
        {
            byte[] fwData = File.ReadAllBytes(fw);
            int tagPos = CheckPromUniqueTag(fwData);
            if (tagPos < 0)
            {
                return false;
            }
            if (fwData == null || fwData.Length <= 0)
            {
                return false;
            }
            byte ck = SkytraqTools.CalcPromRawCheckSum(fwData);

            GPS_RESPONSE rep = GPS_RESPONSE.NONE;
            if (downloadBaudIdx != g.GetBaudRateIndex())
            {
                rep = g.ConfigureSerialPort(2000, downloadBaudIdx, Attributes.Temporarily);
                if (GPS_RESPONSE.ACK != rep)
                {
                    return false;
                }
                g.ReOpen(downloadBaudIdx);
            }

            rep = g.SendLoaderDownload(2000, downloadBaudIdx, true);
            if (GPS_RESPONSE.OK != rep)
            {
                return false;
            }

            rep = g.UploadLoader(2000, LoaderData.PhoenixLoader);
            if (GPS_RESPONSE.OK != rep)
            {
                return false;
            }

            rep = g.SendRomBinSize3(3000, fwData, ck, tagPos, 0);
            if (GPS_RESPONSE.OK != rep)
            {
                return false;
            }

            const int nFlashBytes = 8 * 1024;
            int lSize = fwData.Length;
            int sentBytes = 0;
            int totalByte = 0;
            int rawItr = 0;
            int failCount = 0;

            pf(0, fwData.Length);
            while (lSize > 0)
            {
                sentBytes = (lSize >= nFlashBytes) ? nFlashBytes : lSize;
                totalByte += sentBytes;

                rep = g.SendDataWaitStringAck(fwData, rawItr, sentBytes, 3000, "OK\0");
                if (rep == GPS_RESPONSE.OK)
                {
                    lSize -= sentBytes;
                    rawItr += nFlashBytes;
                    pf(rawItr, fwData.Length);
                }
                else
                {
                    if (++failCount > 0)
                    {
                        return false;
                    }
                    continue;
                }
                failCount = 0;
            }
            rep = g.WaitStringAck(10000, "END\0");
            if (GPS_RESPONSE.OK != rep)
            {
                return false;
            }
            pf(fwData.Length, fwData.Length);
            return true;
        }
